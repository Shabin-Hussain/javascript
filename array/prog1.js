//array

//to display an array

weekdays=['monday','tuesday','thursday','wednesday']

console.log(weekdays);


//total number of items in an array
console.log(weekdays.length);

//print wednesday from the given array
console.log(weekdays[3]);
console.log(weekdays[2]);

//add friday to given array
//push
weekdays.push('friday')
console.log(weekdays);

//add 3 to the given array
weekdays.push(3)
console.log(weekdays);

//remove 3 from the given array
//pop
weekdays.pop();

console.log(weekdays);

//add sunday to the given array

weekdays.push('sunday')
console.log(weekdays);

//add sunday as first item
//unshift
weekdays.unshift('sunday')
console.log(weekdays);

weekdays.unshift('saturday')
console.log(weekdays);

//remove item from the start
//shift
weekdays.shift()
console.log(weekdays);





